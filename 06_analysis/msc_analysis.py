def cost_performance_model():
    print(f"\n{'=' * 80}")
    print("  COST-PERFORMANCE MODEL — Pod-Hours → Cloud Pricing")
    print(f"{'=' * 80}")
    
    # AWS EKS pricing (t3.medium: $0.0416/hr, ~2 vCPU, 4GB)
    # Each pod uses 100m CPU = 0.1 vCPU
    # Cost per pod-hour on t3.medium: $0.0416 * (0.1/2) = $0.00208
    # But more realistically, billing is per-node, so:
    # Node cost per hour: $0.0416 (on-demand t3.medium)
    # Pods per node: ~20 (2000m / 100m)
    # Effective cost per pod-hour: $0.0416 / 20 = $0.00208
    
    COST_PER_POD_HOUR = 0.00208  # USD, AWS t3.medium effective
    GCP_COST_PER_POD_HOUR = 0.00195  # GCP e2-medium effective
    AZURE_COST_PER_POD_HOUR = 0.00215  # Azure B2s effective
    
    print(f"\n  Pricing basis:")
    print(f"    AWS EKS (t3.medium):    ${COST_PER_POD_HOUR:.5f}/pod-hour")
    print(f"    GCP GKE (e2-medium):    ${GCP_COST_PER_POD_HOUR:.5f}/pod-hour")
    print(f"    Azure AKS (B2s):        ${AZURE_COST_PER_POD_HOUR:.5f}/pod-hour")
    print(f"    Pod resource request:   100m CPU (0.1 vCPU)")
    print()
    
    # Compute pod-hours for each scenario
    print(f"  {'Sc':>3} {'Thresh':>6} {'WL':>7} {'Dur(min)':>8} {'MaxPods':>7} {'Pod-Hrs':>8} {'AWS$/run':>9} {'$/month':>9} {'$/year':>9}")
    print(f"  {'-' * 80}")
    
    cost_by_threshold = {50: [], 70: [], 90: []}
    cost_data = []
    
    for sc_id, sc in sorted(data.items()):
        duration_hrs = sc['duration_min'] / 60.0
        max_pods = int(np.mean(sc['max_pods']))
        
        # Compute pod-hours from timeline
        timeline = sc['pod_timeline']
        pod_hours = 0
        for i in range(len(timeline)):
            t_start = timeline[i][0]
            pods = timeline[i][1]
            if i + 1 < len(timeline):
                t_end = timeline[i+1][0]
            else:
                t_end = sc['duration_min']
            segment_hours = (t_end - t_start) / 60.0
            pod_hours += pods * segment_hours
        
        aws_cost = pod_hours * COST_PER_POD_HOUR
        
        # Monthly projection: assume workload repeats
        # Steady: 8hrs/day * 22 days = 176 hrs/month
        # Step: 4 times/day * 22 days = 88 occurrences
        # Bursty: continuous, 720 hrs/month
        # Spike: 2 times/day * 22 days = 44 occurrences
        if sc['workload'] == 'Steady':
            monthly_multiplier = 176 / (sc['duration_min'] / 60)
        elif sc['workload'] == 'Step':
            monthly_multiplier = 88
        elif sc['workload'] == 'Bursty':
            monthly_multiplier = 720 / (sc['duration_min'] / 60)
        else:  # Spike
            monthly_multiplier = 44
        
        monthly_cost = aws_cost * monthly_multiplier
        yearly_cost = monthly_cost * 12
        
        cost_by_threshold[sc['threshold']].append(monthly_cost)
        cost_data.append({
            'id': sc_id, 'threshold': sc['threshold'], 'workload': sc['workload'],
            'pod_hours': pod_hours, 'aws_cost': aws_cost,
            'monthly': monthly_cost, 'yearly': yearly_cost
        })
        
        print(f"  {sc_id:3d} {sc['threshold']:5d}% {sc['workload']:>7s} {sc['duration_min']:7d}m {max_pods:7d} {pod_hours:8.3f} ${aws_cost:8.5f} ${monthly_cost:8.2f} ${yearly_cost:8.2f}")
    
    # Summary by threshold
    print(f"\n  {'=' * 60}")
    print(f"  THRESHOLD COST COMPARISON (monthly, all workloads combined)")
    print(f"  {'=' * 60}")
    for t in [50, 70, 90]:
        total = sum(cost_by_threshold[t])
        print(f"    T{t}%: ${total:.2f}/month  |  ${total*12:.2f}/year")
    
    # Savings calculation
    t50_total = sum(cost_by_threshold[50])
    t70_total = sum(cost_by_threshold[70])
    t90_total = sum(cost_by_threshold[90])
    
    # NOTE: All 12 Nginx scenarios used max_pods=1 — HPA never triggered.
    # Therefore pod-hour costs are equal across all thresholds in practice.
    # Any cost differences below arise from the spike scenario response time
    # differences only (different avg_resp affects theoretical efficiency).
    # Cost savings percentages should NOT be presented as empirical findings.
    # They are illustrative projections of what savings WOULD occur if scaling
    # had been triggered. Treat as theoretical framework only.
    print(f"\n  COST MODEL NOTE: All scenarios used 1 pod — no scaling observed.")
    print(f"  Values below are theoretical projections, not empirical measurements.")
    print(f"\n  PROJECTED SAVINGS (theoretical):")
    if t50_total > t90_total:
        saving_50_90 = t50_total - t90_total
        print(f"    T90% projected saving: ${saving_50_90:.2f}/month vs T50% ({saving_50_90/t50_total*100:.1f}% — theoretical only)")
    if t50_total > t70_total:
        saving_50_70 = t50_total - t70_total
        print(f"    T70% projected saving: ${saving_50_70:.2f}/month vs T50% ({saving_50_70/t50_total*100:.1f}% — theoretical only)")
    
    return cost_data


