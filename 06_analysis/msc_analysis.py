    ax1.spines['right'].set_visible(False)
    
    # RIGHT: Bar chart — monthly cost by threshold
    thresh_monthly = {}
    for t in [50, 70, 90]:
        total = 0
        for sc in data.values():
            if sc['threshold'] == t:
                pod_hours = 0
                for i, (tm, p) in enumerate(sc['pod_timeline']):
                    t_end = sc['pod_timeline'][i+1][0] if i+1 < len(sc['pod_timeline']) else sc['duration_min']
                    pod_hours += p * (t_end - tm) / 60
                total += pod_hours * COST_PER_POD_HOUR
        thresh_monthly[t] = total * 720  # scale to monthly
    
