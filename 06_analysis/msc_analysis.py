def fig_effect_size_heatmap():
    """Effect size heatmap across workloads and threshold pairs."""
    fig, ax = plt.subplots(figsize=(8, 5))
    
    workloads = ['Steady', 'Step', 'Bursty', 'Spike']
    pairs = ['50% vs 70%', '50% vs 90%', '70% vs 90%']
    
    effect_matrix = np.zeros((4, 3))
    
    for i, wl in enumerate(workloads):
        scenarios = {sc['threshold']: sc for sc in data.values() if sc['workload'] == wl}
        pair_thresholds = [(50, 70), (50, 90), (70, 90)]
        for j, (t1, t2) in enumerate(pair_thresholds):
            r1 = scenarios[t1]['avg_resp']
            r2 = scenarios[t2]['avg_resp']
            pooled_sd = np.sqrt((np.var(r1, ddof=1) + np.var(r2, ddof=1)) / 2)
            if pooled_sd > 0:
                effect_matrix[i, j] = abs((np.mean(r1) - np.mean(r2)) / pooled_sd)
            else:
                effect_matrix[i, j] = 0
    
    im = ax.imshow(effect_matrix, cmap='YlOrRd', aspect='auto', vmin=0, vmax=5)
    
    ax.set_xticks(range(3))
    ax.set_xticklabels(pairs, fontsize=10)
    ax.set_yticks(range(4))
    ax.set_yticklabels(workloads, fontsize=10)
    
    for i in range(4):
        for j in range(3):
            val = effect_matrix[i, j]
            magnitude = "neg." if val < 0.2 else "small" if val < 0.5 else "med." if val < 0.8 else "LARGE"
            color = 'white' if val > 2 else 'black'
            ax.text(j, i, f'd={val:.1f}\n({magnitude})', ha='center', va='center', 
                   fontsize=9, fontweight='bold', color=color)
    
    plt.colorbar(im, ax=ax, label="Cohen's d (absolute)")
    ax.set_title("Figure 5.6: Illustrative Effect Size Heatmap — Threshold Impact on Response Time\n(Cohen's d — Spike scenarios only are statistically meaningful; others show d=0)", 
                fontsize=11, fontweight='bold')
    
    plt.tight_layout()
    fig.savefig(f'{OUT}/fig56_effect_size_heatmap.png')
    plt.close()
    print("✓ Figure 5.6: Effect size heatmap")


