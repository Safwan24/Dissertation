def statistical_analysis():
    print("=" * 80)
    print("  DESCRIPTIVE ANALYSIS — RESPONSE TIME & FAILURE RATE SUMMARY")
    print("  NOTE: Single run per scenario. Values [v,v,v] are repeated for")
    print("  compatibility with statistical functions — not independent runs.")
    print("=" * 80)
    
    # --- Per-scenario descriptive stats ---
    print(f"\n{'Sc':>3} {'Thresh':>6} {'WL':>7} | {'AvgResp':>8} {'SD':>6} {'CV':>6} | {'Fail%':>6} {'SD':>6} | {'ScaleDel':>8} {'SD':>6}")
    print("-" * 80)
    
    results = []
    for sc_id, sc in sorted(data.items()):
        avg_r = np.mean(sc['avg_resp'])
        sd_r = np.std(sc['avg_resp'], ddof=1)
        cv_r = (sd_r / avg_r * 100) if avg_r > 0 else 0
        
        avg_f = np.mean(sc['fail_pct'])
        sd_f = np.std(sc['fail_pct'], ddof=1)
        
        delays = [d for d in sc['scale_delay'] if d is not None]
        if delays:
            avg_d = np.mean(delays)
            sd_d = np.std(delays, ddof=1)
            delay_str = f"{avg_d:8.1f} {sd_d:6.1f}"
        else:
            avg_d = None
            sd_d = None
            delay_str = "     N/A    N/A"
        
        print(f"{sc_id:3d} {sc['threshold']:5d}% {sc['workload']:>7s} | {avg_r:8.1f} {sd_r:6.2f} {cv_r:5.1f}% | {avg_f:6.2f} {sd_f:6.3f} | {delay_str}")
        
        results.append({
            'id': sc_id, 'threshold': sc['threshold'], 'workload': sc['workload'],
            'avg_resp_mean': avg_r, 'avg_resp_sd': sd_r, 'avg_resp_cv': cv_r,
            'fail_mean': avg_f, 'fail_sd': sd_f,
            'delay_mean': avg_d, 'delay_sd': sd_d,
        })
    
    # --- Effect Size: Cohen's d ---
    # NOTE: Cohen's d below is ILLUSTRATIVE ONLY.
    # All values are single-run repeated as [v,v,v] so pooled_sd=0 for most
    # scenarios (identical values → d=0 or undefined). Only Spike scenarios
    # (which differ across thresholds) produce meaningful d values.
    # Do not cite these as statistically valid effect sizes.
    print(f"\n{'=' * 80}")
    print("  ILLUSTRATIVE EFFECT SIZE (Cohen's d) — Spike scenarios only are meaningful")
    print("  WARNING: Non-spike d values = 0 due to identical repeated data, not real effect")
    print(f"{'=' * 80}")
    print("  |d| < 0.2: negligible | 0.2-0.5: small | 0.5-0.8: medium | > 0.8: large\n")
    
    workloads = ['Steady', 'Step', 'Bursty', 'Spike']
    thresholds = [50, 70, 90]
    
    for wl in workloads:
        scenarios_by_thresh = {}
        for sc_id, sc in data.items():
            if sc['workload'] == wl:
                scenarios_by_thresh[sc['threshold']] = sc
        
        print(f"  {wl} workload:")
        for i, t1 in enumerate(thresholds):
            for t2 in thresholds[i+1:]:
                if t1 in scenarios_by_thresh and t2 in scenarios_by_thresh:
                    r1 = scenarios_by_thresh[t1]['avg_resp']
                    r2 = scenarios_by_thresh[t2]['avg_resp']
                    pooled_sd = np.sqrt((np.var(r1, ddof=1) + np.var(r2, ddof=1)) / 2)
                    if pooled_sd > 0:
                        d = (np.mean(r1) - np.mean(r2)) / pooled_sd
                    else:
                        d = 0
                    
                    magnitude = "negligible" if abs(d) < 0.2 else "small" if abs(d) < 0.5 else "medium" if abs(d) < 0.8 else "LARGE"
                    print(f"    T{t1}% vs T{t2}% (Avg Response): d = {d:+.2f} ({magnitude})")
        
        f1 = scenarios_by_thresh[50]['fail_pct']
        f3 = scenarios_by_thresh[90]['fail_pct']
        pooled_sd_f = np.sqrt((np.var(f1, ddof=1) + np.var(f3, ddof=1)) / 2)
        if pooled_sd_f > 0:
            d_f = (np.mean(f1) - np.mean(f3)) / pooled_sd_f
            magnitude_f = "negligible" if abs(d_f) < 0.2 else "small" if abs(d_f) < 0.5 else "medium" if abs(d_f) < 0.8 else "LARGE"
            print(f"    T50% vs T90% (Failure Rate):  d = {d_f:+.2f} ({magnitude_f})")
        print()
    
    # --- 95% Confidence Intervals ---
    # NOTE: CI values below are NOT statistically valid.
    # All runs are identical repeats [v,v,v] so SE=0 and CI=[mean,mean].
    # Included for framework completeness only — do not cite in dissertation.
    print(f"{'=' * 80}")
    print("  ILLUSTRATIVE CI (single-run data — intervals are not statistically meaningful)")
    print(f"{'=' * 80}")
    
    try:
        from scipy import stats as scipy_stats
        t_crit = scipy_stats.t.ppf(0.975, df=2)
        print(f"\n  t-critical (95%, df=2): {t_crit:.3f}")
        print(f"  NOTE: SE=0 for all non-varied scenarios → CI collapses to point estimate\n")
        print(f"  {'Scenario':>35} {'Metric':>8} {'Mean':>8} {'CI':>20}")
        print(f"  {'-'*75}")
        for sc_id, sc in sorted(data.items()):
            vals = sc['avg_resp']
            mean = np.mean(vals)
            se = np.std(vals, ddof=1) / np.sqrt(3)
            ci_low = mean - t_crit * se
            ci_high = mean + t_crit * se
            label = f"S{sc_id} T{sc['threshold']}% {sc['workload']}"
            print(f"  {label:>35} {'AvgResp':>8} {mean:8.1f} [{ci_low:8.1f}, {ci_high:8.1f}]")
    except ImportError:
        print("  scipy not available — skipping CI")
    
    return results


